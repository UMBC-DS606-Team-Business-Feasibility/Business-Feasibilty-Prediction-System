def _slide_business_categories(prs, data):
    from pptx.util import Inches
    slide = _add_section_header(prs, "Best Markets per Business Category",
                                 "Top metro for each of the 9 business categories")
    bs = data["business_scores"]
    rows = []
    for biz in sorted(bs["business_type"].unique()):
        sub = bs[bs["business_type"] == biz].sort_values("business_feasibility_score", ascending=False)
        if not sub.empty:
            top = sub.iloc[0]
            rows.append({
                "Business": biz,
                "Top metro": top["metro_title"],
                "Score": f"{top['business_feasibility_score']:.1f}",
                "Strategy": top.get("entry_strategy", "")
            })
    df = pd.DataFrame(rows)
    _add_table(slide, df, Inches(0.4), Inches(1.5), Inches(12.5), Inches(5.0))


