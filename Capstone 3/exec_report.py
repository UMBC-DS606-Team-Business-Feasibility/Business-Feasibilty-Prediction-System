def build_executive_pptx(output_path: Path = None) -> Path:
    """Build the executive PowerPoint deck. Returns the path written."""
    from pptx import Presentation
    from pptx.util import Inches

    out = output_path or (OUTPUT_DIR / "executive_summary.pptx")
    data = _read_outputs()

    prs = Presentation()
    # 16:9 widescreen
    prs.slide_width  = Inches(13.333)
    prs.slide_height = Inches(7.5)

    _add_title_slide(prs, data)
    _slide_data_sources(prs, data)
    _slide_kpis(prs, data)
    _slide_top_metros(prs, data)
    _slide_business_categories(prs, data)
    _slide_clusters(prs, data)
    _slide_models(prs, data)
    _slide_features(prs, data)

    prs.save(str(out))
    return out


# ── PDF generator (text-only, no extra dependency) ───────────────────────────
def build_executive_pdf(output_path: Path = None) -> Path:
    """Build a simple PDF executive summary. Pure-stdlib (no reportlab)."""
    out = output_path or (OUTPUT_DIR / "executive_summary.pdf")
    data = _read_outputs()
    fr = data["full_ranking"].head(10)
    md = data["metadata"]

    def _grab(text, label):
        for L in text.split("\n"):
            if L.startswith(label):
                return L.split(":", 1)[1].strip()
        return ""

    lines = [
        "Metropolitan Business Feasibility - Executive Summary",
        "=" * 72,
        "",
        f"Metros analysed:       {md['selected_metros']}",
        f"Business categories:   {md['business_types_supported']}",
        f"CBP rows analysed:     {md['raw_cbp_rows_used']:,}",
        "",
        "Data stack: CBP 2023 + ACS 2024 + BLS 2024 + BEA RPP 2024",
        "",
        "Model Performance (test set):",
        f"  Best classifier:   {_grab(data['clf_report'], 'Best classifier')}",
        f"  Test Accuracy:     {_grab(data['clf_report'], 'Test Accuracy')}",
        f"  Test F1:           {_grab(data['clf_report'], 'Test F1')}",
        f"  Test ROC-AUC:      {_grab(data['clf_report'], 'Test ROC-AUC')}",
        f"  Best regressor:    {_grab(data['reg_report'], 'Best regressor')}",
        f"  Test R-squared:    {_grab(data['reg_report'], 'Test R2')}",
        "",
        "Top 10 Metros (by Opportunity Score, post-RPP-adjustment):",
        "-" * 72,
    ]
    for _, row in fr.iterrows():
        lines.append(
            f"  {int(row['rank']):>2}. {row['metro_title']:<48} "
            f"score={row['opportunity_score']:.3f}"
        )
    lines.extend([
        "",
        "Key insight: BEA Regional Price Parities re-shaped the rankings.",
        "  - San Francisco fell from #2 to #4 (cost-of-living index 116).",
        "  - Boston fell from top-5 to #13 (CoL 108).",
        "  - Midland TX rose to #2 (CoL 96).",
        "  - Sioux Falls and Bismarck entered top 6 (CoL ~91).",
        "",
        "Generated automatically from outputs/* by exec_report.py",
    ])
    text = "\n".join(lines)

    # Write a minimal valid PDF using only stdlib
    _write_text_pdf(out, text)
    return out


def _write_text_pdf(path: Path, body: str) -> None:
    """Write a single-page PDF containing the given text. No external libs."""
    # Sanitise & escape PDF reserved chars
    def esc(s):
        return s.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")

    # Build PDF content stream
    lines = body.split("\n")
    leading = 14  # line spacing in points
    stream_lines = ["BT", "/F1 10 Tf", f"{leading} TL", "60 760 Td"]
    for i, ln in enumerate(lines):
        if i == 0:
            stream_lines.append(f"({esc(ln)}) Tj")
        else:
            stream_lines.append(f"T* ({esc(ln)}) Tj")
    stream_lines.append("ET")
    stream = "\n".join(stream_lines).encode("latin-1", errors="replace")

    objects = []
    # 1: Catalog
    objects.append(b"<< /Type /Catalog /Pages 2 0 R >>")
    # 2: Pages
    objects.append(b"<< /Type /Pages /Kids [3 0 R] /Count 1 >>")
    # 3: Page
    objects.append(
        b"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] "
        b"/Contents 4 0 R /Resources << /Font << /F1 5 0 R >> >> >>"
    )
    # 4: Content stream
    objects.append(
        f"<< /Length {len(stream)} >>\nstream\n".encode("latin-1") + stream + b"\nendstream"
    )
    # 5: Font (built-in Courier — monospace, looks like a report)
    objects.append(b"<< /Type /Font /Subtype /Type1 /BaseFont /Courier >>")

    pdf = bytearray(b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n")
    offsets = []
    for i, obj in enumerate(objects, start=1):
        offsets.append(len(pdf))
        pdf += f"{i} 0 obj\n".encode() + obj + b"\nendobj\n"
    xref_start = len(pdf)
    pdf += f"xref\n0 {len(objects)+1}\n0000000000 65535 f \n".encode()
    for off in offsets:
        pdf += f"{off:010d} 00000 n \n".encode()
    pdf += (
        f"trailer\n<< /Size {len(objects)+1} /Root 1 0 R >>\n"
        f"startxref\n{xref_start}\n%%EOF\n"
    ).encode()

    path.write_bytes(bytes(pdf))


def build_metro_pptx(metro_title: str, business_type: str | None = None,
                     output_path: Path | None = None) -> Path:
    """Build a one-metro PowerPoint dossier (used by dashboard 'Export' button).

    `metro_title` is the full metro name as it appears in `full_metro_ranking.csv`.
    `business_type` is optional (if given, includes that business's score).
    """
    from pptx import Presentation
    from pptx.util import Inches, Pt

    data = _read_outputs()
    fr = data["full_ranking"]
    bs = data["business_scores"]

    row = fr[fr["metro_title"] == metro_title]
    if row.empty:
        raise ValueError(f"Metro not found: {metro_title}")
    row = row.iloc[0]

    out = output_path or (OUTPUT_DIR / f"metro_dossier_{metro_title.replace(',', '').replace(' ', '_')[:60]}.pptx")
    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Title slide
    s = prs.slides.add_slide(prs.slide_layouts[6])
    s.background.fill.solid()
    s.background.fill.fore_color.rgb = _rgb(INDIGO_DARK)
    tx = s.shapes.add_textbox(Inches(0.6), Inches(2.4), Inches(12.4), Inches(1.6))
    p = tx.text_frame.paragraphs[0]
    p.text = metro_title
    p.runs[0].font.size = Pt(40); p.runs[0].font.bold = True
    p.runs[0].font.color.rgb = _rgb(WHITE)
    sub = s.shapes.add_textbox(Inches(0.6), Inches(4.0), Inches(12.4), Inches(0.6))
    sp = sub.text_frame.paragraphs[0]
    sp.text = (f"Metro Dossier  ·  Rank #{int(row['rank'])} of {len(fr)}  ·  "
               f"Region: {row['region']}")
    sp.runs[0].font.size = Pt(16); sp.runs[0].font.color.rgb = _rgb(INDIGO_300)

    # KPI slide
    s2 = _add_section_header(prs, "At a Glance", "Key economic indicators for this metro")
    cards = [
        ("Opp. Score",      f"{row['opportunity_score']:.3f}",     INDIGO_500),
        ("Median Income",   f"${row['median_income']:,.0f}",       INDIGO_500),
        ("Real Income",     f"${row.get('real_median_income', row['median_income']):,.0f}", EMERALD_500),
        ("Unemployment",    f"{row['unemployment_rate']:.2f}%",    AMBER_500),
        ("Population",      f"{row['population']:,.0f}",           SLATE_500),
        ("CoL Index (BEA)", f"{row.get('cost_of_living_index', 100):.1f}", AMBER_500),
    ]
    for i, (label, value, color) in enumerate(cards):
        col = i % 3
        rowi = i // 3
        _add_kpi_card(s2,
                      Inches(0.4 + col * 4.3),
                      Inches(1.6 + rowi * 1.7),
                      Inches(4.0), Inches(1.4),
                      label, value, color)

    # Business scores for this metro
    s3 = _add_section_header(prs, "Business Feasibility Scores", f"Scores across all 9 categories for {metro_title}")
    metro_biz = bs[bs["metro_title"] == metro_title].copy()
    if not metro_biz.empty:
        metro_biz = metro_biz.sort_values("business_feasibility_score", ascending=False)
        cols = ["business_type", "business_feasibility_score", "feasibility_band", "entry_strategy"]
        cols = [c for c in cols if c in metro_biz.columns]
        biz_show = metro_biz[cols].copy()
        if "business_feasibility_score" in biz_show.columns:
            biz_show["business_feasibility_score"] = biz_show["business_feasibility_score"].map("{:.1f}".format)
        biz_show = biz_show.rename(columns={
            "business_type": "Business", "business_feasibility_score": "Score",
            "feasibility_band": "Band", "entry_strategy": "Strategy",
        })
        _add_table(s3, biz_show, Inches(0.4), Inches(1.5), Inches(12.5), Inches(5.0))

    prs.save(str(out))
    return out


